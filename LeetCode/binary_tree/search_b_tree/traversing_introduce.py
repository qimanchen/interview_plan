"""
前序遍历首先访问根节点，然后遍历左子树，最后遍历右子树。
"""

# 中序遍历
"""
中序遍历是先遍历左子树，然后访问根节点，然后遍历右子树。
通常来说，对于二叉搜索树，我们可以通过中序遍历得到一个递增的有序序列。
（数据结构介绍 – 二叉搜索树）
"""

# 后序遍历
"""
后序遍历是先遍历左子树，然后遍历右子树，最后访问树的根节点。
"""

